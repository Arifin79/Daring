package com.arifin.daringschool.Model;

public class Course {
    String assignmentName;
    String assignmentDate;
    String assignmentFileTask;
    String submitDate;
    String assignmentFileSubmit;
    String score;
    String classes;
    String nameStudent;

    public String getNameStudent() {
        return nameStudent;
    }

    public void setNameStudent(String nameStudent) {
        this.nameStudent = nameStudent;
    }

    public String getClasses() {
        return classes;
    }

    public void setClasses(String classes) {
        this.classes = classes;
    }

    public String getAssignmentName() {
        return assignmentName;
    }

    public void setAssignmentName(String assignmentName) {
        this.assignmentName = assignmentName;
    }

    public String getAssignmentDate() {
        return assignmentDate;
    }

    public void setAssignmentDate(String assignmentDate) {
        this.assignmentDate = assignmentDate;
    }

    public String getAssignmentFileTask() {
        return assignmentFileTask;
    }

    public void setAssignmentFileTask(String assignmentFileTask) {
        this.assignmentFileTask = assignmentFileTask;
    }

    public String getSubmitDate() {
        return submitDate;
    }

    public void setSubmitDate(String submitDate) {
        this.submitDate = submitDate;
    }

    public String getAssignmentFileSubmit() {
        return assignmentFileSubmit;
    }

    public void setAssignmentFileSubmit(String assignmentFileSubmit) {
        this.assignmentFileSubmit = assignmentFileSubmit;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
